package com.android.shadow.model.output;

import java.util.ArrayList;

/**
 * Created by singhgharjyot on 6/9/2017.
 */

public class GetAllOccupationResponse {


    private String message;

    private String status;

    private Data data;

    public String getMessage ()
    {
        return message;
    }

    public void setMessage (String message)
    {
        this.message = message;
    }

    public String getStatus ()
    {
        return status;
    }

    public void setStatus (String status)
    {
        this.status = status;
    }

    public Data getData ()
    {
        return data;
    }

    public void setData (Data data)
    {
        this.data = data;
    }



    public class Data {
        private ArrayList<Occupations> occupations;

        private String pageSize;

        private String pageIndex;

        public ArrayList<Occupations> getOccupations ()
        {
            return occupations;
        }

        public void setOccupations (ArrayList<Occupations> occupations)
        {
            this.occupations = occupations;
        }

        public String getPageSize ()
        {
            return pageSize;
        }

        public void setPageSize (String pageSize)
        {
            this.pageSize = pageSize;
        }

        public String getPageIndex ()
        {
            return pageIndex;
        }

        public void setPageIndex (String pageIndex)
        {
            this.pageIndex = pageIndex;
        }
    }

    public class Occupations
    {
        private String id;

        private String description;

        private String name;

        public String getId ()
        {
            return id;
        }

        public void setId (String id)
        {
            this.id = id;
        }

        public String getDescription ()
        {
            return description;
        }

        public void setDescription (String description)
        {
            this.description = description;
        }

        public String getName ()
        {
            return name;
        }

        public void setName (String name)
        {
            this.name = name;
        }

        @Override
        public String toString()
        {
            return "ClassPojo [id = "+id+", description = "+description+", name = "+name+"]";
        }
    }




}
